package javaapplication101;

public class OpUnarios {

    public static void main(String[] args) {

        int valor = 15;
        System.out.println(valor);//15
        System.out.println(++valor);//16
        System.out.println(valor--);//16
        System.out.println(valor++);//15
        System.out.println(--valor);//15

        System.out.println(valor);//15
        
        System.out.println();
        
        

    }

}
